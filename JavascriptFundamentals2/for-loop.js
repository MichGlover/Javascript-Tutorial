"use strict";

/**First part of the loop is the counter. We use let because the counter will be updated by the loop.
 *
 * The second part is the condition to be evaluated before each iteration of the loop. If true the loop keeps running if not the loop stops.
 *
 *
 * The third part should increase the value after each iteration else the loop never ends
 */
for (let rep = 1; rep <= 10; rep++) {
  console.log(`Lifting weights repetition ${rep}`);
}
